    void preencherTabela(){
    
     modelo = (DefaultTableModel) jTable.getModel();
        modelo.setColumnIdentifiers(new Object[]{"Numero da conta","Saldo","Tipo"});
        modelo.setRowCount(0);
    for(ContaBancaria banco: Banco.getContas() ){
    
  modelo.addRow(new Object[]{
  
  
  banco.getNumeroDaConta(),
      banco.getSaldo(),
      banco.getTipo()
  
  });
    
    
    
    
    }
    
    jTable.setModel(modelo);
    
    }
    
