    void lbl(ContaBancaria conta){
    if(Banco.getContas()==null)lblConta.setText("");
    
      lblConta.setText("Numero: "+conta.getNumeroDaConta());
    lblContas1.setText("Saldo : "+conta.getSaldo());
    lblContas2.setText("Tipo  : "+conta.getTipo());
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
  if((cbxTipoConta.getSelectedItem().toString().equalsIgnoreCase(""))||(txtnrDconta.getText().isEmpty())||(txtDeposito.getText().isEmpty())) JOptionPane.showMessageDialog(this,"Preencha todos os Campos","Erro",JOptionPane.ERROR_MESSAGE);else{
ContaBancaria conta;
  if(cbxTipoConta.getSelectedItem().toString().equalsIgnoreCase("Conta Corrente")){
  
  conta = new ContaCorrente();
  
  
  }else conta = new ContaPoupanca();


  conta.tipoT();
  conta.setNumeroDaConta(txtnrDconta.getText());
  conta.setSaldo(Double.parseDouble(txtDeposito.getText()));
  
  if(Banco.inserir(conta)){JOptionPane.showMessageDialog(this,"feio com sucesso!");preencherTabela(); }else JOptionPane.showMessageDialog(this,"Operacao falhada!");
  } 
  
    txtDeposito.setText("");
    txtnrDconta.setText("");
    cbxTipoConta.setSelectedItem("");
  
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelecionarActionPerformed
 String chave = txtPesq.getText();
   if(!(chave.isEmpty())){
    conta = Banco.procurarConta(Integer.parseInt(chave));
   if((conta!=null)){
   
   jPanelMudar.setVisible(true);
       lbl(conta);
   
   
   } else JOptionPane.showMessageDialog(this,"Conta inexistente");
   
   
   }else JOptionPane.showMessageDialog(this,"Preencha todos os Campos","Erro",JOptionPane.ERROR_MESSAGE);

  conta =null;
    }//GEN-LAST:event_btnSelecionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed


  if(Banco.remover(conta)){
      preencherTabela();
      lbl(conta);
  JOptionPane.showMessageDialog(this,"Operacao efetuada com sucesso!");
  conta=null;
  }

       
    }//GEN-LAST:event_btnRemoverActionPerformed

