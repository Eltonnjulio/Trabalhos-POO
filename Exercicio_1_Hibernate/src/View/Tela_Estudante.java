    void inicio(){
    rbGSexo.add(rbFemenino);
    rbGSexo.add(rbMasculino);
    dcNascimento.setLocale(new Locale("PT","PT"));
    txtDataReg.setEditable(false);
    DateFormat f = DateFormat.getInstance();
    txtDataReg.setText( f.format(new Date()));
    }
    
     void clean(){
    txtNome.setText(" ");
    dcNascimento.setDate(null);
    rbGSexo.clearSelection(); 
    }
     void preencherTabela(){
     
     modelo = (DefaultTableModel) tabelaEstudantes.getModel();
     
     modelo.setColumnIdentifiers(new String[]{"Codigo","Nome","Data de ingresso","Data de nascimento"});
     
     for(String[] auxiliar: DAO.lerMVC()){
     
     modelo.addRow(new String[]{ auxiliar[0],auxiliar[1],auxiliar[3]  ,auxiliar[3],auxiliar[4]});

     }
     
     }
