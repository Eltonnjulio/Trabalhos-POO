    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
   if(txtNome.getText().isEmpty() || txtDataReg.getText().isEmpty() || rbGSexo.getSelection().isSelected()||dcNascimento.equals(null)){JOptionPane.showMessageDialog(this,"Preencha os espacos vazios ");} else{
  if(DAO.gravar(txtNome.getText(), rbGSexo.getSelection().toString(), new Date(), dcNascimento.getDate())){ JOptionPane.showMessageDialog(this,"Gravado com sucesso!"); clean();}
  else JOptionPane.showMessageDialog(this, "Erro ao gravar");
    }//GEN-LAST:event_btnSalvarActionPerformed
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
     
   int resposta = JOptionPane.showConfirmDialog(this,"Deseja mesmo eliminar ?","Eliminar",JOptionPane.YES_NO_OPTION);
  if(resposta == JOptionPane.YES_NO_OPTION){
  
      SimpleDateFormat sDF = new SimpleDateFormat(txtDataReg.getText());
  DAO.eliminar(codigo, txtNome.getText(), rbGSexo.getSelection().toString(),sDF.getCalendar().getTime() , dcNascimento.getDate());
  
  }

    }//GEN-LAST:event_btnEliminarActionPerformed

